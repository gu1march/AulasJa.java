
public class Matematica {
	
	//Tornando um método statico faz com que não seja necessário instanciar um objeto para usar um método, você chama o método chamando a classe (Ex:Matematica.somar)//
	public static int somar(int a, int b) {
		return a+b;
	}
	
	public static int subtrair(int a, int b) {
		return a-b;
	}
	
	
}
